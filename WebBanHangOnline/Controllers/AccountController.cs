using System;
using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Security.Claims;
using System.Threading.Tasks;
using System.Web;
using System.Web.Mvc;
using Microsoft.AspNet.Identity;
using Microsoft.AspNet.Identity.Owin;
using Microsoft.Owin.Security;
using WebBanHangOnline.Models;

namespace WebBanHangOnline.Controllers
{
    public class AccountController : Controller
    {
        private ApplicationSignInManager _signInManager;
        private ApplicationUserManager _userManager;

        public AccountController()
        {
        }

        public AccountController(ApplicationUserManager userManager, ApplicationSignInManager signInManager)
        {
            UserManager = userManager;
            SignInManager = signInManager;
        }

        public ApplicationSignInManager SignInManager
        {
            get
            {
                return _signInManager ?? HttpContext.GetOwinContext().Get<ApplicationSignInManager>();
            }
            private set
            {
                _signInManager = value;
            }
        }

        public ApplicationUserManager UserManager
        {
            get
            {
                return _userManager ?? HttpContext.GetOwinContext().GetUserManager<ApplicationUserManager>();
            }
            private set
            {
                _userManager = value;
            }
        }

        public async Task<ActionResult> Profile()
        {
            var user = await UserManager.FindByNameAsync(User.Identity.Name);
            var item = new CreateAccountViewModel();
            item.FullName = user.CustomerName;
            item.Phone = user.CustomerPhone;
            item.Address = user.CustomerAddress;
            item.UserName = user.UserName;
            item.Email = user.Email;
            return View(item);
        }

        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> PostProfile(CreateAccountViewModel req)
        {
            var user = await UserManager.FindByEmailAsync(req.Email);
            user.FullName = req.FullName;
            user.Phone = req.Phone;
            user.Address = req.Address;
            var rs = await UserManager.UpdateAsync(user);
            if (rs.Succeeded)
            {
                return RedirectToAction("Profile");
            }
            return View(req);
        }


        [AllowAnonymous]
        public ActionResult Login(string returnUrl)
        {
            var model = new LoginViewModelClient
            {
                ReturnUrl = returnUrl
            };
            return View(model);
        }

